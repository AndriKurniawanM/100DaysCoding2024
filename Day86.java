public class Day86 {
    public static void main(String[] args) {
        // Membuat dan mengisi array dengan nilai-nilai
        int[] isi = {10, 20, 30, 40, 50};

        // Mencetak elemen array menggunakan loop
        System.out.println("Elemen-elemen pada array adalah:");
        for (int output : isi) {
            System.out.println(output);
        }
    }
}
